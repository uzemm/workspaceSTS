package com.kh.library.book.vo;

public class HopeBookVO {
	private String hopeCode;
	private String hopeName;
	private String hopeWriter;
	private String hopePublisher;
	private String memId;
	
	public String getHopeCode() {
		return hopeCode;
	}
	public void setHopeCode(String hopeCode) {
		this.hopeCode = hopeCode;
	}
	public String getHopeName() {
		return hopeName;
	}
	public void setHopeName(String hopeName) {
		this.hopeName = hopeName;
	}
	public String getHopeWriter() {
		return hopeWriter;
	}
	public void setHopeWriter(String hopeWriter) {
		this.hopeWriter = hopeWriter;
	}
	public String getHopePublisher() {
		return hopePublisher;
	}
	public void setHopePublisher(String hopePublisher) {
		this.hopePublisher = hopePublisher;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
}
