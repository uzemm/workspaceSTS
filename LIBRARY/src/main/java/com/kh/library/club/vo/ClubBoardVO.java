package com.kh.library.club.vo;

public class ClubBoardVO {
	private String cbBoardNum;
	private String cbBoardTitle;
	private String cbBoardContent;
	private String cbBoardDate;
	private String memId;
	private String clubCode;
	
	public String getCbBoardNum() {
		return cbBoardNum;
	}
	public void setCbBoardNum(String cbBoardNum) {
		this.cbBoardNum = cbBoardNum;
	}
	public String getCbBoardTitle() {
		return cbBoardTitle;
	}
	public void setCbBoardTitle(String cbBoardTitle) {
		this.cbBoardTitle = cbBoardTitle;
	}
	public String getCbBoardContent() {
		return cbBoardContent;
	}
	public void setCbBoardContent(String cbBoardContent) {
		this.cbBoardContent = cbBoardContent;
	}
	public String getCbBoardDate() {
		return cbBoardDate;
	}
	public void setCbBoardDate(String cbBoardDate) {
		this.cbBoardDate = cbBoardDate;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getClubCode() {
		return clubCode;
	}
	public void setClubCode(String clubCode) {
		this.clubCode = clubCode;
	}
	
}
